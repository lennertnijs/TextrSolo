package com.Textr.InputHandler;

public final class Inputs {

    /**
     * CR -> 13 decimal \r
     * LF -> 10 decimal \n
     * CR LF -> 13 10 \r\n
     */

    public static final int ENTER = 13;
    public static final int CTRL_N = 14;
    public static final int CTRL_P = 16;
    public static final int CTRL_R = 18;
    public static final int CTRL_S = 19;
    public static final int CTRL_T = 20;
    public static final int ESCAPE = 27;
    public static final int SPACE = 32;
    public static final int N_UPPER = 78;
    public static final int Y_UPPER = 89;
    public static final int N_LOWER = 110;
    public static final int Y_LOWER = 121;
    public static final int BACKSPACE = 127;
}
